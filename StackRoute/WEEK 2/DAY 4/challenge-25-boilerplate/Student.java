public class Student {
    private String studentName;
    private int studentRollNo;
    private double mathMarks;
    private double sciMarks;
    private double engMarks;
    private double langMarks;
    private double socialMarks;

    public String getStudentName() {
        return studentName;
    }

    public int getStudentRollNo() {
        return studentRollNo;
    }

    public double getMathMarks() {
        return mathMarks;
    }

    public double getSciMarks() {
        return sciMarks;
    }

    public double getEngMarks() {
        return engMarks;
    }

    public double getLangMarks() {
        return langMarks;
    }

    public double getSocialMarks() {
        return socialMarks;
    }

    public void setStudentName(String studentName) {
        this.studentName = studentName;
    }

    public void setStudentRollNo(int studentRollNo) {
        this.studentRollNo = studentRollNo;
    }

    public void setMathMarks(double mathMarks) {
        this.mathMarks = mathMarks;
    }

    public void setSciMarks(double sciMarks) {
        this.sciMarks = sciMarks;
    }

    public void setEngMarks(double engMarks) {
        this.engMarks = engMarks;
    }

    public void setLangMarks(double langMarks) {
        this.langMarks = langMarks;
    }

    public void setSocialMarks(double socialMarks) {
        this.socialMarks = socialMarks;
    }

}
